// Given two numbers X and Y. Print the summation and multiplication and subtraction of these 2 numbers.
#include<iostream>
using namespace std;
int main(){
    long long X,Y;
    cin>>X>>Y;
    cout<<X<<" + "<<Y<<" = "<<X+Y<<endl;
    cout<<X<<" * "<<Y<<" = "<<X*Y<<endl;
    cout<<X<<" - "<<Y<<" = "<<X-Y<<endl;
    return 0;
}    